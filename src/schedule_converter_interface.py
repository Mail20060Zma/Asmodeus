def csv_to_schedule_dict(csv_file_path):
    schedule = {}
    schedule['Monday'] = {}
    schedule['Tuesday'] = {}
    schedule['Wednesday'] = {}
    schedule['Thursday'] = {}
    schedule['Friday'] = {}
    schedule['Saturday'] = {}
    for key_adder in schedule.keys():
        schedule[key_adder] = {'06:50':[], '08:30':[], '10:15':[], '12:00':[], '14:15':[], '16:00':[], '17:40':[], '19:15':[], '20:50':[]}
    schedule['All_subject'] = []
    temp_list_subject = []
    temp_list = []
    temp_list_teacher = []
    temp_list_groupe = {}

    with open(csv_file_path, mode='r', encoding='utf-8') as file:
        reader = csv.DictReader(file)
        for row in reader:
            day = row['Day']
            time = row['Time']
            auditory = row['Auditory']
            full_subject = row['Subject']
            group = row['Group']
            teacher = row['Teacher']
            institute = row['Institute']
            
            full_subject = full_subject.rstrip()
            
            short_subject = subject_short_names[full_subject]
            system_subject = subject_system_names[full_subject]
            parsed_subject = full_subject[:15]+'-/'+full_subject[15:30]
            
            subject_obj = Drop_menu_subject(
                parsed_subject,
                short_subject + "  " + group,
                auditory,
                system_subject,
                20,
                institute,
                teacher,
                group,
                full_name = full_subject
            )

            if full_subject not in temp_list_subject:
                subject_all_obj = Drop_menu_subject(
                    parsed_subject,
                    short_subject,
                    '',
                    full_subject,
                    20
                ) 
                temp_list_subject.append(full_subject)
                schedule['All_subject'].append(subject_all_obj)

            if full_subject not in temp_list:
                schedule[full_subject] = []
                temp_list.append(full_subject)
                temp_list_groupe[full_subject] = []
            
            for temp_teacher in teacher.split(',  '):
                if temp_teacher not in temp_list_teacher:
                    Fio = temp_teacher.split()
                    subject_teacher_obj = Drop_menu_subject(
                        '/'.join(temp_teacher.split()),
                        ' '.join([Fio[0]] + [f"{Fio[1][0]}." if len(Fio) > 1 and Fio[1] else ""] + [f"{Fio[2][0]}." if len(Fio) > 2 and Fio[2] else ""]),
                        '',
                        temp_teacher,
                        20
                    ) 
                    temp_list_teacher.append(temp_teacher)
                    schedule[full_subject].append(subject_teacher_obj)

            if group not in temp_list_groupe[full_subject]:
                subject_groupe_obj = Drop_menu_subject(
                    group,
                    group,
                    '',
                    group,
                    20
                ) 
                temp_list_groupe[full_subject].append(group)
                schedule[full_subject].append(subject_groupe_obj)
            
            schedule[day][time].append(subject_obj)
    for day in ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday']:
        for time in schedule[day].keys():
            schedule[day][time].insert(0, Drop_menu_subject(
                '',
                '-УДАЛИТЬ-',
                '',
                '',
                20
            ))
    for subject_temp in temp_list_subject:
        schedule[subject_temp] = sorted(
            schedule[subject_temp],
            key=lambda x: (0 if not (len(x.list_name) > 2 and x.list_name[2] == '-') else 1,
                           x.list_name if not (len(x.list_name) > 2 and x.list_name[2] == '-') 
                           else int(x.list_name.split('-')[1])))


    return dict(schedule)

